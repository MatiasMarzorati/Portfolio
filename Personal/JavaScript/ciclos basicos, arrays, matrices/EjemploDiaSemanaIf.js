// Dia de la semana
let diaSemana = 1;

if(diaSemana == 1){
    console.log('Lunes');
}
else if(diaSemana == 2){
    console.log('Martes');
}
else if(diaSemana == 3){
    console.log('Miercoles');
}
else if(diaSemana == 4){
    console.log('Jueves');
}
else if(diaSemana == 5){
    console.log('Viernes');
}
else if(diaSemana == 6){
    console.log('Sabado');
}
else if(diaSemana == 7){
    console.log('Domingo');
}
else{
    console.log(`Dia semana erroneo ${diaSemana}`);
}