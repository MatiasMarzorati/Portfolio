// Operadores de Asignacion
let miNumero = 10;
console.log(miNumero);

miNumero = 20;
console.log(miNumero);

// Operador +=
miNumero += 5;
console.log(miNumero);

// Operador -=
miNumero -= 3;
console.log(miNumero);

// Operador *=
miNumero *= 2;
console.log(miNumero);

// Operador /=
miNumero /= 4;
console.log(miNumero);

// Operador %=
miNumero %= 2;
console.log(miNumero);

// Exponente **=
miNumero = 8;
miNumero **= 2;// 8 * 8 = 64
console.log(miNumero);